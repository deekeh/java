package Calculation;

public class MathOperation
{
	public int addx(int a, int b)
	{
		return (a+b);
	}
	public int subtractx(int a, int b)
	{
		return (a-b);
	}
	public int multiplyx(int a, int b)
	{
		return (a*b);
	}
	public double dividex(int a, int b)
	{
		return (a/b);
	}
}